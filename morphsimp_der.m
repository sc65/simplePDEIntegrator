function y=morphsimp_der(x,i)
    g = .4 ; d=.01; alph=0.05;
    y=zeros(4,1);
    y(1)=-d*x(1);
    if(i==1) 
        y(1)=y(1)+g;
    end
    y(2)=alph*x(1)^4/(0.0025^4+x(1)^4)*0.00001^4/(0.00001^4+x(3)^4+x(4)^4)-d*x(2);
    y(3)=alph*x(1)^4/(0.1^4+x(1)^4)*0.1^4/(0.1^4+x(2)^4+x(4)^4)-d*x(3);
    y(4)=alph*x(1)^4/(2^4+x(1)^4)*1^4/(1^4+x(2)^4+x(3)^4)-d*x(4);